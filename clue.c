#include "clue.h"

clue_State *clue_init (void)
{
  clue_State *L;

  if ((L = luaL_newstate()))
      luaL_openlibs(L);
  return L;
}

void clue_close(clue_State *L)
{
  lua_close(L);
}

void clue_do(clue_State *L, const char *code)
{
  assert (luaL_loadstring(L, code) == 0);
  clue_docall(L, 0, 1);
}

